void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	k;

	k = size - 1;
	while (k != 0)
	{
		i = -1;
		while (i++ < k)
		{
			if (tab[i] > tab[k])
			{
				j = tab[i];
				tab[i] = tab[k];
				tab[k] = j;
			}
		}
		k--;
	}
}
