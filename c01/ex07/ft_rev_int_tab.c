void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;

	i = -1;
	while (i++ < size / 2 - 1)
	{
		j = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = j;
	}
}
